#1463 블로그 참고

n = int(input())
d = [0] * (n+1)
# d[0]이 첫번째이긴 하지만 편의상 d[1]을 첫번째로 설정하기 위해 n+1개의 리스트로 초기화

for i in range(2, n+1):
    d[i] = d[i-1] + 1
    #일단 이전 수에서 1을 더한 것으로 가정
    if i % 2 == 0:
        d[i] = min(d[i], d[i // 2] + 1)
    #2로 나누어 떨어지는 수가 -1한 수보다 횟수가 적을 경우 재설정
    if i % 3 == 0:
        d[i] = min(d[i], d[i // 3] + 1)
    #3로 나누어 떨어지는 수가 -1한 수보다 횟수가 적을 경우 재설정
        
print(d[n])